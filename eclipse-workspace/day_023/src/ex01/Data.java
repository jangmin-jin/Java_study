package ex01;

// 1차 배열
public class Data {
	private String num1;
	private String num2;
	private String num3;
	private String num4;
	private String num5;
	
	public Data(String num1, String num2, String num3, String num4, String num5) {
		this.num1 = num1;
		this.num2 = num2;
		this.num3 = num3;
		this.num4 = num4;
		this.num5 = num5;
	}
	
	public String getNum1() {
		return num1;
	}
	public String getNum2() {
		return num2;
	}
	public String getNum3() {
		return num3;
	}
	public String getNum4() {
		return num4;
	}
	public String getNum5() {
		return num5;
	}
	
	
}