public class OperatorEx03 {
    public static void main(String[] args){
        
        // 비교 / 논리연산자
        int a = 10;
        int b = 5;
        
        System.out.println( a == b );   // false
        System.out.println( a < b );    // false
        System.out.println( a > b );    // true
    }
}