public class BooleanEx01{
    public static void main(String[] args){
        // 변수의 선언
        // 논리형
        boolean b1;

        // 초기화
        b1 = true;

        // 선언과 동시에 초기화
        boolean b2 = false;

        System.out.println( b1 );
        System.out.println( b2 );

        // 맞지 않는 자료형을 넣으면 에러 발생
        // boolean b3 = "false";
        // System.out.println( b3 );
    }
}