public class DoubleEx01 {
    public static void main(String[] args){
        // 실수형 float, double
        // int i = 2.5; 정수형이라 사용 불가능
        // System.out.println( i );

        // float f = 2.5; : float는 별도의 선언처리가 없으면 에러난다
        // System.out.println( f );

        double d = 2.5;
        System.out.println( d );

        float f = 2.5f;
        System.out.println( f );

        double d_2 = 1.0e3;
        System.out.println( d_2 );
    }
}